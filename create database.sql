CREATE DATABASE NOTAS;
USE NOTAS;
CREATE TABLE NOTAS1 (
    Student_ID VARCHAR(255) PRIMARY KEY,
    Student_Age VARCHAR(10),
    Sex VARCHAR(10),
    High_School_Type VARCHAR(50),
    Scholarship VARCHAR(10),
    Additional_Work VARCHAR(5),
    Sports_activity VARCHAR(5),
    Transportation VARCHAR(50),
    Weekly_Study_Hours INT,
    Attendance VARCHAR(50),
    Reading VARCHAR(5),
    Notes VARCHAR(5),
    Listening_in_Class VARCHAR(5),
    Project_work VARCHAR(5),
    Grade VARCHAR(5)
);

